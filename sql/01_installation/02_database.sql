-- =============================================================================
-- VUE — accueil (une ligne : effectifs patients / médecins / RDV)
--        GET /api/accueil
-- =============================================================================
CREATE OR REPLACE VIEW v_accueil_resume AS
SELECT
    (SELECT COUNT(*) FROM patients)   AS nb_patients,
    (SELECT COUNT(*) FROM medecins)  AS nb_medecins,
    (SELECT COUNT(*) FROM rendez_vous WHERE statut NOT IN ('annule', 'termine')) AS nb_rdv
FROM dual;


-- =============================================================================
-- VUE — RDV « actifs » (planifié ou confirmé), toutes dates — même périmètre que nb_rdv
--        GET /api/accueil/prochains-rdv — tri dans la requête Python
-- =============================================================================
CREATE OR REPLACE VIEW v_prochains_rdv AS
SELECT
    r.id,
    r.date_heure,
    r.motif,
    r.statut,
    r.duree_minutes,
    p.nom           AS patient_nom,
    p.prenom        AS patient_prenom,
    m.nom           AS medecin_nom,
    m.specialite    AS medecin_specialite
FROM rendez_vous r
JOIN patients p ON p.id = r.patient_id
JOIN medecins m ON m.id = r.medecin_id
WHERE r.statut NOT IN ('annule', 'termine');


