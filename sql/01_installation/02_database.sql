-- =============================================================================
-- TABLES & INDEX
-- =============================================================================
CREATE TABLE medecins (
    id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nom         VARCHAR2(100) NOT NULL,
    specialite  VARCHAR2(120) NOT NULL
);

CREATE TABLE patients (
    id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nom         VARCHAR2(100) NOT NULL,
    prenom      VARCHAR2(100) NOT NULL,
    cin         VARCHAR2(8),
    telephone   VARCHAR2(30),
    email       VARCHAR2(120),
    CONSTRAINT chk_patients_cin CHECK (
        cin IS NULL OR REGEXP_LIKE(cin, '^[A-Z]{1,2}[0-9]{5,6}$')
    ),
    CONSTRAINT uk_patients_cin UNIQUE (cin)
);

CREATE TABLE app_users (
    id              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username        VARCHAR2(80) NOT NULL UNIQUE,
    password_hash   VARCHAR2(512) NOT NULL
);

CREATE TABLE rendez_vous (
    id              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    patient_id      NUMBER NOT NULL,
    medecin_id      NUMBER NOT NULL,
    date_heure      TIMESTAMP NOT NULL,
    motif           VARCHAR2(500),
    statut          VARCHAR2(30) DEFAULT 'planifie'
                    CONSTRAINT chk_statut CHECK (statut IN ('planifie', 'confirme', 'annule', 'termine')),
    duree_minutes   NUMBER DEFAULT 30,
    CONSTRAINT fk_rdv_patient FOREIGN KEY (patient_id) REFERENCES patients(id),
    CONSTRAINT fk_rdv_medecin FOREIGN KEY (medecin_id) REFERENCES medecins(id)
);

CREATE INDEX idx_rdv_date ON rendez_vous(date_heure);
CREATE INDEX idx_rdv_patient ON rendez_vous(patient_id);

CREATE TABLE rdv_suppressions_historique (
    id              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ancien_rdv_id   NUMBER NOT NULL,
    patient_id      NUMBER NOT NULL,
    medecin_id      NUMBER NOT NULL,
    date_heure      TIMESTAMP NOT NULL,
    motif           VARCHAR2(500),
    statut          VARCHAR2(30),
    duree_minutes   NUMBER,
    supprime_le     TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE INDEX idx_rdv_hist_supprime ON rdv_suppressions_historique(supprime_le);


