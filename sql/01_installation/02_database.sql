-- =============================================================================
-- TRIGGER — historique sur DELETE
-- =============================================================================
CREATE OR REPLACE TRIGGER tr_rdv_hist_after_delete
    AFTER DELETE ON rendez_vous
    FOR EACH ROW
BEGIN
    INSERT INTO rdv_suppressions_historique (
        ancien_rdv_id,
        patient_id,
        medecin_id,
        date_heure,
        motif,
        statut,
        duree_minutes,
        supprime_le
    ) VALUES (
        :OLD.id,
        :OLD.patient_id,
        :OLD.medecin_id,
        :OLD.date_heure,
        :OLD.motif,
        :OLD.statut,
        :OLD.duree_minutes,
        SYSTIMESTAMP
    );
END;
/
